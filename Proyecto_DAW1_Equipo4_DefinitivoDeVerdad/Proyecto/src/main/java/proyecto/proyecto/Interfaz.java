    private void modificarModificarProfesorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarModificarProfesorActionPerformed
        String dato = txtDatoModificarProfesor.getText();
        String dni = txtDniModificarProfesor.getText();
        if (datoModificarProfesor.getSelectedItem().toString().equals("Nombre")) {
            profesores.modificarNombre(dato, dni);
            JOptionPane.showMessageDialog(this, "Nombre Modificado");
        }
        if (datoModificarProfesor.getSelectedItem().toString().equals("Apellidos")) {
            profesores.modificarApellidos(dato, dni);
            JOptionPane.showMessageDialog(this, "Apellidos Modificados");
        }
        if (datoModificarProfesor.getSelectedItem().toString().equals("Contraseña")) {
            profesores.modificarContraseñaporDNI(dato, dni);
            JOptionPane.showMessageDialog(this, "Contraseña Modificada");
        }
        if (datoModificarProfesor.getSelectedItem().toString().equals("Correo")) {
            profesores.modificarCorreo(dato, dni);
            JOptionPane.showMessageDialog(this, "Correo Modificado");
        }
        if (datoModificarProfesor.getSelectedItem().toString().equals("Departamento")) {
            profesores.modificarDepartamento(dato, dni);
            JOptionPane.showMessageDialog(this, "Departamento Modificado");
        }
// TODO add your handling code here:
    }//GEN-LAST:event_modificarModificarProfesorActionPerformed

